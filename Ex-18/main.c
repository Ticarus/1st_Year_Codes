#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    printf("10 raised to the 3rd power is %.1f.\n",pow(10,3));   //pow() function raises a value to a power
    printf("16's square root is %.1f",sqrt(16));        //sqrt() function returns the square root of a value
    return 0;
}
